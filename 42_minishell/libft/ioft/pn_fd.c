#include "libft.h"
#include <stdio.h>

void	pn_fd(int n, int fd)
{
	int		i[3];

	i[0] = 0;
	i[1] = i_len(n);
	i[2] = 0;
	if (n < 0)
	{
		write(fd, "-", 1);
		i[0]++;
	}
	while (i[0] < i[1])
	{
		i[2] = n % 10;
		i[2] = (i[2] < 0) ? -i[2] + '0' : i[2] + '0';
		write(fd, &i[2], 1);
		i[0]++;
		n = n / 10;
	}
}
