#include "libft.h"

int		c_is_print(int c)
{
	if (c == 32)
		return (1);
	else if ((c > 32 && c < 48) || (c > 57 && c < 65))
		return (1);
	else if ((c > 90 && c < 97) || (c > 122 && c < 127))
		return (1);
	else if (c > 47 && c < 58)
		return (1);
	else if (c > 64 && c < 91)
		return (1);
	else if (c > 96 && c < 123)
		return (1);
	return (0);
}
