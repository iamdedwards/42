#include "minishell.h"

int					ms_echo(t_ms_ctx *ctx)
{
	int				i;

	i = 1;
	if (ctx->args[i])
	{
		while (ctx->args[i])
		{
			ps(ctx->args[i]);
			if (ctx->args[i + 1])
				pc(' ');
			i++;
		}
	}
	ps("\n");
	return (1);
}
