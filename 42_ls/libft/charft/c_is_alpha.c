#include "libft.h"

int		c_is_alpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	else if (c >= (122 - 25) && c <= 122)
		return (1);
	else
		return (0);
}
