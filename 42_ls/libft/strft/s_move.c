#include "libft.h"

char			*s_move(char *s, int n)
{
	int				len;
	int				i;

	i = 0;
	len = s_len(s);
	while (s[i + n])
	{
		s[i] = s[i + n];
		i++;
	}
	s[i] = 0;
	return (s);
}
