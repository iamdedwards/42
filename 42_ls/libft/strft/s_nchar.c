#include "libft.h"

size_t	s_nchar(const char *str, char c)
{
	size_t n;
	size_t i;

	i = 0;
	n = 0;
	while (str[i])
	{
		if (str[i] == c)
			n++;
		i++;
	}
	return (n);
}
