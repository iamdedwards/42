#include "libft.h"

int				s_nto_inner(char *s, char *s2)
{
	int				i;
	int				len;

	len = s_len(s2);
	i = 0;
	while (s[i])
	{
		if (s_n_cmp(&s[i], s2, len) == 0)
			return (i);
		i++;
	}
	return (-1);
}
