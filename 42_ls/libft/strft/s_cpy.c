#include "libft.h"

char			*s_cpy(char *dest, const char *src)
{
	size_t i;

	i = 0;
	if (!src)
		return ("");
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
