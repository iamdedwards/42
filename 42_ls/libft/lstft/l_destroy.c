#include "libft.h"

void			l_destroy(t_list **a_ref, void (*f)(void **))
{
	t_list			*b;

	while ((*a_ref))
	{
		(b) = (*a_ref);
		(*a_ref) = (*a_ref)->next;
		if (f)
		{
			f((void **)&((b)->content));
		}
		x_del((void **)(&b));
	}
}
